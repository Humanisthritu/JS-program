// ques: swapping of number (interchange the value given by user of two variable)
/*
    ex: a = 10, b = 20
    after swapping a = 20, b = 10
*/

let a = 5;
let b = 20;

console.log(`value of a is ${a} and value of b is ${b}`);

let temp = a; // temp = 5
a = b; // a = 20
b = temp; // b = 5

console.log(`value of a is ${a} and value of b is ${b}`);
